#' Add two numbers
#' @param a A number
#' @param b A number
#'
#' @returns the sum of a and b
#' @export
#'
#' @examples
#' add_numbers(2, 2)
add_numbers <- function(a, b) {
  return (a + b)
}

#' Multiply two numbers
#' @param a A number
#' @param b A number
#'
#' @returns the product of a and b
#' @export
#'
#' @examples
#' multiply_numbers(2, 2)
multiply_numbers <- function(a, b) {
  return (a * b)
}
